// Métodos auxiliares da tela de cadastro de corretoras
// - parseDecimal(String valor): converte o texto digitado em BigDecimal,
//   tratando valores nulos, strings vazias e substituindo vírgula por ponto.
//   Caso o valor seja inválido, retorna ZERO e exibe uma mensagem de validação.

    private BigDecimal parseDecimal(String valor) {
        if (valor == null) {
            return BigDecimal.ZERO;
        }
        String v = valor.trim().replace(",", ".");
        if (v.isEmpty()) {
            return BigDecimal.ZERO;
        }
        try {
            return new BigDecimal(v);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Valor inválido: " + valor, "Validação", JOptionPane.WARNING_MESSAGE);
            return BigDecimal.ZERO;
        }
    }

    // - limparCampos(): reseta os campos de entrada da tela (nome, taxas e observações),
//   deixando-os prontos para um novo cadastro.
    
    private void limparCampos() {
        txtCorretora.setText("");
        txtTaxaFixa.setText("");
        txtTaxaPercentual.setText("");
        txtObservacoes.setText("");
    }
    
    
    
    private void carregarCorretorasNaTabela() {
    try {
        CorretoraDAO dao = new CorretoraDAO();
        java.util.List<Corretora> lista = dao.listarTodas();

        // Modelo da tabela
        javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel(
            new Object[]{"ID", "Corretora", "Taxa Fixa", "Taxa Percentual", "Observações"}, 0
        );

        // Preenche a tabela
        for (Corretora c : lista) {
            model.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                c.getTaxaFixa(),
                c.getTaxaPercentual(),
                c.getObservacoes()
            });
        }

        TabelaListagemDeCorretoras.setModel(model);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao carregar corretoras: " + e.getMessage(),
                "Erro", JOptionPane.ERROR_MESSAGE);
    }
}
    
    

