    private void BntInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BntInserirActionPerformed

// 
        try {
            Corretora c = new Corretora();
            c.setNome(txtCorretora.getText());
            c.setTaxaFixa(parseDecimal(txtTaxaFixa.getText()));
            c.setTaxaPercentual(parseDecimal(txtTaxaPercentual.getText()));
            c.setObservacoes(txtObservacoes.getText());

            // Validação via Service
            CorretoraService service = new CorretoraService();
            service.validar(c);

            // Persistência via DAO
            CorretoraDAO dao = new CorretoraDAO();
            dao.inserir(c);

            JOptionPane.showMessageDialog(this, "Corretora cadastrada com sucesso!");
            limparCampos();

        } catch (IllegalArgumentException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Validação", JOptionPane.WARNING_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar corretora: " + e.getMessage(),
                    "Erro", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_BntInserirActionPerformed

