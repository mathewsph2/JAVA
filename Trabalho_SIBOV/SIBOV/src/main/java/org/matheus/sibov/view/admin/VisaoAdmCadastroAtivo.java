    private void carregarTabelaAtivos() {
        try {
            AtivoDAO dao = new AtivoDAO();
            List<Ativo> lista = dao.listarTodos();

            DefaultTableModel modelo
                    = (DefaultTableModel) TabelaListagemAtivos.getModel();

            modelo.setRowCount(0);

            for (Ativo a : lista) {
                modelo.addRow(new Object[]{
                    // a.getId(),
                    a.getTicker(),
                    a.getTipo().name(),
                    a.getSegmento(),
                    a.getObservacoes()
                });
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Erro ao carregar ativos: " + e.getMessage(),
                    "Erro",
                    JOptionPane.ERROR_MESSAGE);
        }
    }
