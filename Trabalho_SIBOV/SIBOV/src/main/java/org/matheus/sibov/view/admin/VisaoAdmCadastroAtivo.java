    private void BtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnExcluirActionPerformed
        // C√≥dido executado quando o usu√°rio clicar no bot√£o excluir: 

        try {
            // 1Ô∏è‚É£ Capturar o ticker do campo
            String ticker = txtTicket.getText().trim();

            // 2Ô∏è‚É£ Validar
            if (ticker.isEmpty()) {
                JOptionPane.showMessageDialog(this,
                        "Informe ou selecione um ativo para excluir.",
                        "Aviso",
                        JOptionPane.WARNING_MESSAGE);
                return;
            }

            // 3Ô∏è‚É£ Confirmar exclus√£o
            int confirm = JOptionPane.showConfirmDialog(
                    this,
                    "Tem certeza que deseja excluir o ativo: " + ticker + "?",
                    "Confirmar Exclus√£o",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.WARNING_MESSAGE
            );

            if (confirm != JOptionPane.YES_OPTION) {
                return;
            }

            // 4Ô∏è‚É£ Chamar o DAO
            AtivoDAO dao = new AtivoDAO();
            dao.deletarPorTicker(ticker);

            // 5Ô∏è‚É£ Limpar campos
            txtTicket.setText("");
            txtSegmento.setText("");
            txtObservacoes.setText("");
            txtValorCota.setText("");
            RadioButtonAcao.setSelected(false);
            RadioButtonFii.setSelected(false);

            // 6Ô∏è‚É£ Mensagem
            JOptionPane.showMessageDialog(this,
                    "üóëÔ∏è Ativo exclu√≠do com sucesso!",
                    "Sucesso",
                    JOptionPane.INFORMATION_MESSAGE);

        } catch (Exception e) {

            JOptionPane.showMessageDialog(this,
                    "Erro ao excluir ativo: " + e.getMessage(),
                    "Erro",
                    JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_BtnExcluirActionPerformed


