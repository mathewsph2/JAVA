    private void BtnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnInserirActionPerformed

        try {
            // 1Ô∏è‚É£ Captura os dados dos campos
            String ticker = txtTicket.getText().trim();
            String segmento = txtSegmento.getText().trim();
            String observacoes = txtObservacoes.getText().trim();
            String valorCotaStr = txtValorCota.getText().trim(); // ‚Üê CAPTURA O VALOR DO CAMPO

            // 2Ô∏è‚É£ Verifica qual RadioButton foi selecionado
            TipoAtivo tipo = null;

            if (RadioButtonAcao.isSelected()) {
                tipo = TipoAtivo.ACAO;
            } else if (RadioButtonFii.isSelected()) {
                tipo = TipoAtivo.FII;
            } else {
                JOptionPane.showMessageDialog(this,
                        "Selecione o tipo de ativo (A√ß√£o ou FII).",
                        "Aviso",
                        JOptionPane.WARNING_MESSAGE);
                return;
            }

            // 3Ô∏è‚É£ Converte e valida o valor da cota
            BigDecimal valorCota = null; // ‚Üê DECLARA A VARI√ÅVEL
            if (!valorCotaStr.isEmpty()) {
                try {
                    valorCota = new BigDecimal(valorCotaStr);
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this,
                            "Valor da cota inv√°lido! Use formato correto (ex: 150.50).",
                            "Erro",
                            JOptionPane.ERROR_MESSAGE);
                    return;
                }
            }

            // 4Ô∏è‚É£ Cria o objeto correto (Acao ou FII)
            Ativo ativo;
            if (tipo == TipoAtivo.ACAO) {
                ativo = new Acao();
            } else {
                ativo = new FII();
            }

            // 5Ô∏è‚É£ Define os valores no objeto
            ativo.setTicker(ticker);
            ativo.setTipo(tipo);
            ativo.setSegmento(segmento);
            ativo.setObservacoes(observacoes);
            ativo.setValorCota(valorCota); // ‚Üê AGORA A VARI√ÅVEL EXISTE

            // 6Ô∏è‚É£ Chama o DAO para salvar no banco
            AtivoDAO dao = new AtivoDAO();
            dao.salvar(ativo);

            // 7Ô∏è‚É£ Exibe mensagem e limpa os campos
            JOptionPane.showMessageDialog(this,
                    "‚úÖ Ativo cadastrado com sucesso!",
                    "Sucesso",
                    JOptionPane.INFORMATION_MESSAGE);

            txtTicket.setText("");
            txtSegmento.setText("");
            txtObservacoes.setText("");
            txtValorCota.setText(""); // ‚Üê LIMPAR CAMPO
            RadioButtonAcao.setSelected(false);
            RadioButtonFii.setSelected(false);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Erro ao salvar ativo: " + e.getMessage(),
                    "Erro",
                    JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }


    }//GEN-LAST:event_BtnInserirActionPerformed

