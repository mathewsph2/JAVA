// Método para preencher a tabela de ativos
/* ---------------------------------------------------------------------------- */
    
    private void carregarTabelaAtivos() {
    AtivoDAO dao = new AtivoDAO();
    List<Ativo> lista = dao.listarTodos();

    DefaultTableModel modelo = (DefaultTableModel) TabelaListarAtivosDisponiveis.getModel();
    modelo.setRowCount(0); // limpa a tabela

    for (Ativo ativo : lista) {
        modelo.addRow(new Object[]{
            ativo.getId(),
            ativo.getTicker(),
            ativo.getTipo().name()
        });
    }
}
    
