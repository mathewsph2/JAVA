    private void BtnCadastrarProventoSelecionadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCadastrarProventoSelecionadoActionPerformed
        
        int linha = TabelaListarAtivosDisponiveis.getSelectedRow();

    if (linha != -1) {
        int idAtivo = (int) TabelaListarAtivosDisponiveis.getValueAt(linha, 0);
        txtIdAtivo.setText(String.valueOf(idAtivo));

        // Buscar ativo no banco para já preencher o tipo de rendimento
        AtivoDAO dao = new AtivoDAO();
        Ativo ativo = dao.buscarPorId(idAtivo);

        if (ativo != null) {
            if (ativo.getTipo().name().equals("FII")) {
                txtTipoProvento.setText("RENDIMENTO_FII");
            } else if (ativo.getTipo().name().equals("ACAO")) {
                txtTipoProvento.setText("DIVIDENDO"); // padrão
            }
        }

        jTabbedPane1.setSelectedIndex(2); // vai para aba "Inserir - Editar Provento"
    } else {
        JOptionPane.showMessageDialog(this, "Selecione um ativo na tabela para cadastrar provento.");
    }
        
    }//GEN-LAST:event_BtnCadastrarProventoSelecionadoActionPerformed

    private void BtnSalvarProventoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSalvarProventoActionPerformed
        
        try {
        Provento provento = new Provento();
        provento.setIdAtivo(Integer.parseInt(txtIdAtivo.getText()));
        provento.setTipo(TipoProvento.valueOf(txtTipoProvento.getText().toUpperCase()));
        provento.setValorPorCota(new BigDecimal(txtValorCota.getText()));
        provento.setDataCom(LocalDate.parse(txtDataCom.getText()));
        provento.setDataEx(LocalDate.parse(txtDataEx.getText()));
        provento.setDataPagamento(LocalDate.parse(txtDataPagamento.getText()));
        provento.setStatus(StatusProvento.valueOf(txtStatus.getText().toUpperCase()));
        provento.setObservacoes(txtObservacoes.getText());

        ProventoDAO dao = new ProventoDAO();
        dao.salvar(provento);

        JOptionPane.showMessageDialog(this, "Provento salvo com sucesso!");
        limparCamposProvento();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao salvar provento: " + e.getMessage());
    }


    }//GEN-LAST:event_BtnSalvarProventoActionPerformed

