    private void carregarTabelaAtivosComProventos() {
   
        
        ProventoDAO dao = new ProventoDAO();
    List<Object[]> lista = dao.listarAtivosComProventos();

    DefaultTableModel modelo = (DefaultTableModel) TabelaAtivosComProventos.getModel();
    modelo.setRowCount(0); // limpa

    for (Object[] linha : lista) {
        modelo.addRow(linha);
    }
        
}
    
    

